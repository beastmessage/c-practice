#include <stdio.h>
#include <stdlib.h>
#include <math.h>
float squareRoot(float num) {
    return sqrt(num);
}
float square(float num) {
    return num * num;
}
float sum(float num1, float num2) {
    return num1 + num2;
}
float subtract(float num1, float num2) {
    return num1 - num2;
}
float multiply(float num1, float num2) {
    return num1 * num2;
}
float divide(float num1, float num2) {
    return num1 / num2;
}